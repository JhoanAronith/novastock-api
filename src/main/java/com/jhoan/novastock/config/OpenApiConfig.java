package com.jhoan.novastock.config;

import io.swagger.v3.oas.models.OpenAPI;
import io.swagger.v3.oas.models.info.Contact;
import io.swagger.v3.oas.models.info.Info;
import io.swagger.v3.oas.models.info.License;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

@Configuration
public class OpenApiConfig {

    @Bean
    public OpenAPI customOpenApi() {
        return new OpenAPI()
                .info(new Info()
                        .title("NovaStock - Sistema de Gestión de Inventarios")
                        .version("1.0")
                        .description("API REST para el control de productos, categorías y procesamiento de pedidos transaccionales.")
                        .contact(new Contact()
                                .name("Jhoan Aronith")
                                .email("jhoanmunozman@gmail.com"))
                        .license(new License()
                                .name("Apache 2.0")
                                .url("http://springdoc.org")));
    }

}
